   public void consultar() {
    try {
        Usuario usuario = new Usuario();
        int idUsuario = usuario.obtenerIdUsuario(numero);
        
        Bolsillo bolsillo = new Bolsillo();
        ResultSet rs = bolsillo.retornarBolsillos(idUsuario);

        Object[] bolsillos = new Object[4];
        DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();

        while (rs.next()) {
            bolsillos[0] = rs.getInt("Id");
            bolsillos[1] = rs.getString("NombreBolsillo");
            bolsillos[2] = rs.getString("SaldoDisponible");
            bolsillos[3] = rs.getString("Meta");

            modelo.addRow(bolsillos);
        }
        
        Tabla.setModel(modelo);
        } catch (Exception e) {
            e.printStackTrace();
         
        }

    }
        
        
