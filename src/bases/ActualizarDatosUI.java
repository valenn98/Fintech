    private void obtenerDatosUsuario() {
        
        Usuario usuario = new Usuario();
        ResultSet resultSet = usuario.obtenerDatos(numero);
        
        
        try {
           
            if (resultSet.next()) {
                String celular = resultSet.getString("Celular");
                String email = resultSet.getString("Email");
                String clave = resultSet.getString("Clave");

                txtCelularActualizar.setText(celular);
                txtEmailActualizar.setText(email);
                txtClaveActualizar.setText(clave);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    private void actualizarDatosUsuario(String nuevoCelular, String nuevoEmail, String nuevaClave) {
        
        try {
            Usuario usuario = new Usuario();
            int filasActualizadas = usuario.actualizarDatos(nuevoCelular, nuevoEmail, nuevaClave, numero);

            if (filasActualizadas > 0) {
                MensajeExito ventanaMensaje = new MensajeExito();
                ventanaMensaje.setVisible(true);
                this.dispose();  
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo actualizar los datos", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

